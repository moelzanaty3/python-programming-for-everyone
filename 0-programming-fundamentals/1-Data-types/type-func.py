# To check the type of an object in Python, use the built-in type() function, just like in the lines of code below
# The type() function either returns the type of the object or returns a new type object based on the arguments passed.

print(type(2))  # <class 'int'>
print(type(2.2))  # <class 'float'>
print(type("2"))  # <class 'str'>
print(type(True))  # <class 'bool'>
